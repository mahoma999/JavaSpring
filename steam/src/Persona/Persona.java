package Persona;

public class Persona {
  private String nombre;

  /**
  * @param nombre
  */
  public Persona(String nombre) {
    this.nombre = nombre;
  }

  /**
   * @return the nombre
   */
  public String getNombre() {
    return nombre;
  }

  /**
   * @param nombre the nombre to set
   */
  public void setNombre(String nombre) {
    this.nombre = nombre;
  }
}
